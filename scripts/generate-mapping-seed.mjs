/**
 * Sprint 5 Step 7 — generate seed migration dari MAPPING_KARYAWAN_FINAL_V2.csv.
 *
 * Reads CSV → emits SQL INSERT statements untuk:
 *   1. public.users (kalau IN_MOM_SAMPLE=YES) dengan UUID deterministic
 *      based on email hash (idempotent across runs)
 *   2. public.user_aliases (seed type) untuk semua alias di kolom ALIASES
 *      (semicolon-separated, additional ke trigger auto-aliases)
 *
 * UUID strategy: sha256(email).slice(0,32) formatted as UUID v4-ish.
 * Bukan auth.users — purely reference data untuk PIC resolver.
 *
 * Usage: node scripts/generate-mapping-seed.mjs > supabase/migrations/<ts>_seed_mom_aliases.sql
 */
import { readFileSync } from 'node:fs';
import { createHash } from 'node:crypto';
import { parse } from 'csv-parse/sync';

const csvPath = 'docs/sample-mom/MAPPING_KARYAWAN_FINAL_V2.csv';
const csv = readFileSync(csvPath, 'utf-8');
const rows = parse(csv, { columns: true, trim: true });

function emailToUuid(email) {
  const hash = createHash('sha256').update(email.toLowerCase()).digest('hex');
  // Format as UUID v4: 8-4-4-4-12, force version 4 nibble + variant
  return [
    hash.slice(0, 8),
    hash.slice(8, 12),
    '4' + hash.slice(13, 16),
    '8' + hash.slice(17, 20),
    hash.slice(20, 32),
  ].join('-');
}

function escapeSql(s) {
  if (s === null || s === undefined || s === '') return 'NULL';
  return "'" + String(s).replace(/'/g, "''") + "'";
}

// Default team for seed users (no team — admin-org-wide reference)
// Use NULL team_id so RLS users_select_same_team_or_self doesn't break.
// But Member RLS requires same team... seed users hidden from Member by default OK.

const out = [];
out.push(`-- Auto-generated by scripts/generate-mapping-seed.mjs
-- Source: ${csvPath}
-- DO NOT EDIT manually. Re-run script kalau master CSV update.

BEGIN;
`);

let userInsertCount = 0;
let aliasInsertCount = 0;

// Skip [UNRESOLVED] rows yang tidak punya email — mereka audit-only entries.
const inMomYesRows = rows.filter(
  (r) => r.IN_MOM_SAMPLE === 'YES' && r.EMAIL && r.EMAIL.trim() !== '',
);

out.push('-- =============================================================');
out.push(`-- 1. Seed ${inMomYesRows.length} users (IN_MOM_SAMPLE=YES rows only)`);
out.push('-- =============================================================');

for (const r of inMomYesRows) {
  const id = emailToUuid(r.EMAIL);
  // role default 'member' — these are reference data, not active users
  // ON CONFLICT skip (id already exists from prior seed run)
  out.push(
    `INSERT INTO public.users (id, email, full_name, role, team_id) VALUES (${escapeSql(id)}, ${escapeSql(r.EMAIL)}, ${escapeSql(r.NAMA)}, 'member', NULL) ON CONFLICT (id) DO NOTHING;`,
  );
  userInsertCount += 1;
}

out.push('');
out.push('-- =============================================================');
out.push('-- 2. Seed user_aliases dari kolom ALIASES (semicolon-separated)');
out.push('--    type=seed, confidence dari CSV CONFIDENCE column.');
out.push('-- =============================================================');

for (const r of inMomYesRows) {
  const userId = emailToUuid(r.EMAIL);
  const aliases = (r.ALIASES || '').split(';').map((s) => s.trim()).filter(Boolean);
  const conf = r.CONFIDENCE || 'HIGH';

  for (const alias of aliases) {
    out.push(
      `INSERT INTO public.user_aliases (user_id, alias_text, alias_type, confidence) VALUES (${escapeSql(userId)}, ${escapeSql(alias)}, 'seed', ${escapeSql(conf)}) ON CONFLICT (user_id, alias_text) DO NOTHING;`,
    );
    aliasInsertCount += 1;
  }
}

out.push('');
out.push('COMMIT;');
out.push('');
out.push(`-- Seeded ${userInsertCount} users + ${aliasInsertCount} aliases (CSV-extra).`);
out.push(`-- Plus auto-created aliases dari users_auto_create_aliases trigger:`);
out.push(`--   = ${userInsertCount} full_name + ${userInsertCount} nickname + ${userInsertCount * 4} honorific`);
out.push(`--   = ${userInsertCount * 6} trigger aliases.`);

console.log(out.join('\n'));
console.error(`Generated: ${userInsertCount} users + ${aliasInsertCount} CSV aliases.`);
